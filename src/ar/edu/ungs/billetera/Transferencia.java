package ar.edu.ungs.billetera;

public class Transferencia {
	private Cuenta origen;
	private Cuenta destino;
	
	public constructor() {
		this.origen=origen;
		this.destino=destino;
		
	}

	@Override
	public String toString() {
		return "Transferencia [origen=" + origen + ", destino=" + destino + "]";
	}
	
	

}
